public class code19 {
	public  static void factor() {
		int num = 8;
		for (int i = 1; i <= num; ++i) {
			if (num % i == 0) {
	        System.out.print(i + " ");
	      }   
	}
	}

	public static void main(String[] args) {
		code19 pp =new code19() ;
		pp.factor();
		

	}

}
